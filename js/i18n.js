export const i18n = {
    currentLang: localStorage.getItem('fpc_lang') || 'es',
    
    init() {
        document.documentElement.lang = this.currentLang;
        this.applyTranslations();
    },
    
    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('fpc_lang', lang);
        document.documentElement.lang = lang;
        this.applyTranslations();
        // Notify components
        window.dispatchEvent(new CustomEvent('lang-changed', { detail: lang }));
    },
    
    translate(key) {
        return translations[this.currentLang] ? (translations[this.currentLang][key] || key) : key;
    },
    
    applyTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = this.translate(key);
            if (translation !== key) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = translation;
                } else if (el.tagName === 'LI' && el.querySelector('span.material-symbols-outlined')) {
                    // Preservar iconos si existen (esto es más complejo, pero para este caso)
                    const icon = el.querySelector('span.material-symbols-outlined').outerHTML;
                    el.innerHTML = `${icon} ${translation}`;
                } else {
                    // Si el elemento tiene hijos que no son texto (como iconos), solo cambiamos el nodo de texto
                    let foundText = false;
                    for (let node of el.childNodes) {
                        if (node.nodeType === 3 && node.textContent.trim().length > 0) {
                            node.textContent = translation;
                            foundText = true;
                            break;
                        }
                    }
                    if (!foundText) el.innerText = translation;
                }
            }
        });
        
        // Update language button styles
        document.querySelectorAll('.lang-btn').forEach(btn => {
            if (btn.getAttribute('data-lang') === this.currentLang) {
                btn.classList.add('border-secondary', 'bg-secondary/10', 'text-secondary');
                btn.classList.remove('border-surface-variant');
            } else {
                btn.classList.remove('border-secondary', 'bg-secondary/10', 'text-secondary');
                btn.classList.add('border-surface-variant');
            }
        });
    }
};
